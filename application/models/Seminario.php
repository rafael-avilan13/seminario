<?php if ( ! defined('BASEPATH')) exit('No direct script access allowed');

class Seminario extends CI_Model
{
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	public function __construct()
	{
		 parent::__construct();
		 $this->load->database();
    }
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public function categorias(){
        $sql="SELECT * 
        FROM categoria";
        $query = $this->db->query($sql);
        if($query->num_rows() > 0)
        {
            return $query->result();
        }else{
            return FALSE;
        }
    }
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}